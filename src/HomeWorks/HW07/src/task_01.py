# inch to cm

def inch_cm(inch):
    return inch * 2.54
    #return {'cm': inch * 2.54}

#cm to inch

def cm_inch(cm):
    return cm * 0.393701



#mile to km

def mile_km(mile):
    return mile * 1.60934

#km to mile

def km_mile(km):
    return km * 0.621371



#lb to kg

def lb_kg(lb):
    return lb * 0.453592

#kg to lb

def kg_lb(kg):
    return kg * 2.20462



#oz to gram

def oz_gr(oz):
    return oz * 28.3495

#gram to oz

def gr_oz(gr):
    return gr * 0.035274



#american  gallon to liter
def gal_lit(gal):
    return gal * 3.78541

#liter to american gallon
def lit_gal(lit):
    return lit * 0.264172



#american pint to liter

def pint_lit(pint):
    return pint * 0.473176

#liter to american pint

def lit_pint(lit):
    return lit * 2.11338
